#include "./common.h"

/**
 * babgbag m = 7
 * 
 * bag n = 3
 */

class Solution
{
public:
    int numDistinct(string s, string t)
    {
        if (s.size() > s.size())
            return 0;
        for (auto &&i : t)
        {
            cout << i << " ";
        }
        cout << endl;
    }
};
